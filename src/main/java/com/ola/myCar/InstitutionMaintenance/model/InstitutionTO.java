package com.ola.myCar.InstitutionMaintenance.model;

public class InstitutionTO {

    private String adress;
    private String email;
    private int phoneNr;

    public InstitutionTO(String adress, String email, int phoneNr) {

        this.adress = this.adress;
        this.email = this.email;
        this.phoneNr = this.phoneNr;
    }




    public String getAdress() {
        return adress;
    }

    public String getEmail() {
        return email;
    }

    public int getPhoneNr() {
        return phoneNr;
    }
}
